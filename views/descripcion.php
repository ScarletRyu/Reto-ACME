      <h2>Sinopsis</h2>
      <p>Peaky Blinders es un drama de BBC que narra la historia de la familia gitana Shelby y su banda de gángsters, un grupo de personas características por sus boinas con cuchillas y dueñas de los asuntos ilegales, que dominan las apuestas clandestinas y se rigen mediante extorsiones.</p>
      <p>La historia comienza en el Birmingham (Reino Unido) de los años 20 cuando un joven Tomas (Tommy) Shelby regresa de la guerra tras combatir contra los franceses para ocuparse del negocio familiar regentado, durante su ausencia, por su tía Polly Gray.</p>
      <p>Sin embargo, una vez que llega a su tierra, todo son problemas. Dirigir un negocio criminal no es tarea fácil, y menos si aún están presente las secuelas psicológicas que conlleva estar en la guerra. Además, los desencuentros políticos hacen que se cree una brecha entre su mejor amigo de la infancia Freddie, con ideas comunistas, y su hermana Ada, quienes mantienen una relación a espaldas de la familia y, sobre todo, de Tommy.</p>
      <p>Las disputas también aparecen en el terreno de lo profesional, pues tiene varios encontronazos con diferentes enemigos, siendo el más importante un capitán de policía recién llegado de Belfast que se ha propuesto limpiar el lugar de criminales, gángsters y todo aquel que esté en contra de las leyes que dicta el país, siendo su principal cometido el de recuperar unas armas interceptadas por los Shelby.</p>
      <p id="last">Peaky Blinders está protagonizada por la estrella Cillian Murphy, protagonista de Luces rojas; y Sam Neill (Plan de Escape). Además, el creador y guionista de la serie se trata nada menos que de Steven Knight, creador de la historia de Promesas del Este.</p>
